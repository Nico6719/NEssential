function mergeLangFiles() {
    const langDir = globalThis.NEST_LangDir || './plugins/NEssential/lang/';
    const filePath = globalThis.langFilePath || (langDir + 'zh_cn.json');

    try {
        if (!fs.existsSync(langDir)) fs.mkdirSync(langDir, { recursive: true });

        let existing = {};
        if (fs.existsSync(filePath)) {
            try { existing = JSON.parse(fs.readFileSync(filePath, 'utf8')); } catch {}
        }

        // 合并：用默认值填充缺失的键，不覆盖已有翻译
        let changed = false;
        for (const [k, v] of Object.entries(defaultLangContent)) {
            if (existing[k] === undefined) { existing[k] = v; changed = true; }
        }

        if (changed) fs.writeFileSync(filePath, JSON.stringify(existing, null, 2), 'utf8');

        // 更新 globalThis.lang 内存数据
        if (globalThis.lang && typeof globalThis.lang.write === 'function') {
            globalThis.lang.write(JSON.stringify(existing));
        }
        globalThis.defaultLangContent = defaultLangContent;

        if (typeof globalThis.randomGradientLog === 'function')
            globalThis.randomGradientLog('语言文件已加载完成');
    } catch (err) {
        if (typeof logger !== 'undefined') logger.error(`I18n 加载失败: ${err.message}`);
    }
}

module.exports = {
    init: mergeLangFiles,
    defaultLangContent,
};
